function v=tineyesearch_ahash(picture1,picture2)
t1=imresize(picture1,[8 8],'bicubic'); %?????????
t2=imresize(picture2,[8 8],'bicubic'); %?????????
t1=round(t1/4);
t2=round(t2/4);
mem1=round(sum(sum(t1))/64);
mem2=round(sum(sum(t2))/64);
for i=1:8
    for j=1:8
        if t1(i,j)>=mem1
            t1(i,j)=1;
        else
            t1(i,j)=0;
        end
        if t2(i,j)>=mem2
            t2(i,j)=1;
        else
            t2(i,j)=0;
        end
    end
end
h=abs(t1-t2);
v=sum(sum(h));